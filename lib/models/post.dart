import 'package:flutter/material.dart';

class Post extends ChangeNotifier {
  int userId;
  int id;
  String title;
  String body;
  Post(this.userId, this.id, this.title, this.body);
  Post.fromJson(Map<String, dynamic> json) {
    userId = json['userId'];
    id = json['id'];
    title = json['title'];
    body = json['body'];
  }
}
